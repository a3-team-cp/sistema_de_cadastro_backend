package br.backend.modelo.enums;

/**
 * Enum que representa o tamanho associado a um produto ou categoria.
 *
 * <p>Este valor é usado para classificar produtos conforme seu porte,
 * embalagem física ou quantidade típica. Também auxilia na organização
 * do catálogo e pode ser utilizado em filtros e relatórios.</p>
 */
public enum Tamanho {

    /** Representa itens de porte pequeno. */
    PEQUENO,

    /** Representa itens de porte médio. */
    MEDIO,

    /** Representa itens de porte grande. */
    GRANDE
}
